package methodStatic;

public class EvenOdd {
	
	public int checkEvenOdd() {
		
		int a = 64;

		if (a % 2 == 0) {
			System.out.println(a + " is Even Number ... !");
		} else {
			System.out.println(a + " is Odd Number ... !");
		}
		
		return a;
		
	}	
		public int checkOddEven() {
			
			int a = 73;

			if (a % 2 == 0) {
				System.out.println(a + " is Even Number ... !");
			} else {
				System.out.println(a + " is Odd Number ... !");
			}
			
			return a;
						
	}

}
